package com.trend.models;

public class PackageInfo {

	private String length;
	
	private String width;
	
	private String height;
	
	private String dimensionUnits;
	
	private String dimensionUnitsCode;
	
	private String weight;
	
	private String weightUnits;
	
	private String weightUnitsCode;
	
	private String description;

	public String getLength() {
		return length;
	}

	public void setLength(String length) {
		this.length = length;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getDimensionUnits() {
		return dimensionUnits;
	}

	public void setDimensionUnits(String dimensionUnits) {
		this.dimensionUnits = dimensionUnits;
	}

	public String getDimensionUnitsCode() {
		return dimensionUnitsCode;
	}

	public void setDimensionUnitsCode(String dimensionUnitsCode) {
		this.dimensionUnitsCode = dimensionUnitsCode;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getWeightUnits() {
		return weightUnits;
	}

	public void setWeightUnits(String weightUnits) {
		this.weightUnits = weightUnits;
	}

	public String getWeightUnitsCode() {
		return weightUnitsCode;
	}

	public void setWeightUnitsCode(String weightUnitsCode) {
		this.weightUnitsCode = weightUnitsCode;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
	
	
}
